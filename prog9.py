# if elif else condition

brand = input("Enter your favourite brand name:").capitalize()
if brand == "Puma":
    print("Puma is no. 1 brand")
elif brand == "Nike":
    print("Nike is no. 2 brand")
elif brand == "Reebok":
    print("Reebok is no. 3 brand")
else:
    print("Other brand are not recommended")